        <h1>TOYOTA</h1>
        <p>&nbsp;&nbsp;Как найти? Табличка Vin Plate где указан код/номер краски кузова на автомобилях <strong>TOYOTA</strong> может находится в нескольких местах: обычно это стойки передних дверей либо под капотом у двигателя. На рисунке снизу можно посмотреть варианты расположения кода краски кузова на Vin Plate c указанием номера/кода краски с привязкой к модели автомобиля.</p>
        <p align="center"><img src="../../public/images/codes/Toyota/toyota.jpg" ></p>
        <p><strong>[1].</strong> Celica, Hilux, Land Cruiser, Previa.</p>
        <p><strong>[2].</strong> Avensis Verso, Camry, Picnic, Yaris Versa.</p>
        <p><strong>[3].</strong> Celica, Corolla, Hiace, Previa, RAV4, Carina.</p>
        <p><strong>[4].</strong> Avalon, Avensis.</p>
        <p><strong>[5].</strong> Corolla, IQ, Yaris, 4 Runner.</p>
        <p><strong>[6].</strong> Corolla, IQ, Yaris.</p>
        <p>&nbsp;&nbsp;На фото внешний вид шильдиков Vin Plate автомобилей <strong>TOYOTA</strong>, где находится номер краски.</p>
        <p align="center"><img src="../../public/images/codes/Toyota/vin_toyota.jpg" ></p>
        <p>&nbsp;&nbsp;Как узнать номер краски? Слева на картинке код краски <strong>8L5</strong>, справа <strong>8N2</strong>.</p>
