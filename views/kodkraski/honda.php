        <h1>HONDA</h1>
        <p>&nbsp;&nbsp;Как найти? Табличка Vin Plate где указан код/номер краски на автомобилях <strong>HONDA</strong> может находится в нескольких местах. На картинке ниже можно посмотреть варианты расположения Vin Plate в зависимости от модели. Это может быть под капотом, на стойках передней двери.</p>
        <p align="center"><img src="../../public/images/codes/Honda/HONDA.jpg" ></p>
        <p><strong>[1].</strong> Accord, Civic, Jazz.</p>
        <p><strong>[2].</strong> Civic(2001), CR-V, FR-V, Stream, S2000.</p>
        <p><strong>[3].</strong> Civic, HRV, Insight, Logo, NSX, Odyssey, S2000.</p>
        <p>&nbsp;&nbsp;На фото примеры расположения табличек Vin Plate автомобилей <strong>HONDA</strong>.</p>
