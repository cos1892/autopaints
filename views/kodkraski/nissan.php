        <h1>NISSAN/DATSUN</h1>
        <p>&nbsp;&nbsp;Как найти? Табличка Vin Plate где указан код/номер краски кузова на автомобилях <strong>Nissan</strong> находится под капотом у стекла либо на стойке правой передней двери. На картинке ниже можно посмотреть варианты расположения Vin Plate в зависимости от модели.</p>
