        <p><strong>[1].</strong> Almera, Kubistar, Micra, Note, Primera,Primastar, Qashqai.</p>
        <p><strong>[2].</strong> 350Z.</p>
        <p><strong>[3].</strong> Micra, Navara, Pathfinder, Patrol, Pick Up, Primera, Tiida, X-Trail.</p>
        <p><strong>[4].</strong> Terrano II.</p>
